    private void cargarIndividual(IndividualVO individual) {
        labelURI.setText(individual.getUri());
        DefaultTableModel model = (DefaultTableModel) tableDatos.getModel();
        for (String key : individual.getPropiedadesPrincipales().keySet()) {
            model.addRow(new String[]{key, individual.getPropiedadesPrincipales().get(key).toString()});
        }
        for (String key : individual.getPropiedadesAvanzadas().keySet()) {
            model.addRow(new String[]{key, individual.getPropiedadesAvanzadas().get(key).toString()});
        }
    }

